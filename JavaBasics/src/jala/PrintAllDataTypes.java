package jala;

public class PrintAllDataTypes {

	public static void main(String[] args) {

		int i = 110;
		System.out.println("integer value: " + i);
		
		boolean b = true;
		System.out.println("Boolean datatype: " + b);
		
		char ch = 'H';
		System.out.println("Char datatype: " + ch);

		String str = "Java";
		System.out.println("String datatype: " + str);

		double d = 190.98;
		System.out.println("Double datatype: " + d);

		float f = 3.14f;
		System.out.println("Float datatype: " + f);

	}

}
